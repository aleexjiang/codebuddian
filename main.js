// src/features/chat/controllers/StreamController.ts
var StreamController = class {
  constructor(stateManager) {
    this.stateManager = stateManager;
  }
  currentAssistantContent = "";
  handleEvent(tabId, event) {
    switch (event.type) {
      case "message": {
        const data = event.data;
        if (data.role === "assistant" || data.delta) {
          if (data.delta) {
            this.currentAssistantContent += data.delta;
          } else if (data.content) {
            this.currentAssistantContent = data.content;
          }
          this.stateManager.updateLastAssistantMessage(tabId, this.currentAssistantContent);
        } else if (data.role === "user" && data.content) {
          const userMsg = {
            id: `msg-${Date.now()}`,
            role: "user",
            content: data.content,
            timestamp: event.timestamp
          };
          this.stateManager.addMessage(tabId, userMsg);
        }
        break;
      }
      case "thinking": {
        const data = event.data;
        const tab = this.stateManager.getActiveTab();
        if (tab) {
          const lastAssistant = [...tab.messages].reverse().find((m) => m.role === "assistant");
          if (lastAssistant) {
            lastAssistant.thinkingContent = data.content || "";
            this.stateManager.updateTab(tabId, {});
          }
        }
        break;
      }
      case "tool_use": {
        const data = event.data;
        if (data.id && data.tool) {
          const tab = this.stateManager.getActiveTab();
          if (tab && tab.messages.length > 0) {
            const lastMsg = tab.messages[tab.messages.length - 1];
            if (!lastMsg.toolCalls) lastMsg.toolCalls = [];
            lastMsg.toolCalls.push({
              id: data.id,
              tool: data.tool,
              args: data.args || {},
              status: "running"
            });
            this.stateManager.updateTab(tabId, {});
          }
        }
        break;
      }
      case "tool_result": {
        const data = event.data;
        if (data.id) {
          const tab = this.stateManager.getActiveTab();
          if (tab && tab.messages.length > 0) {
            const lastMsg = tab.messages[tab.messages.length - 1];
            if (lastMsg.toolCalls) {
              const toolCall = lastMsg.toolCalls.find((tc) => tc.id === data.id);
              if (toolCall) {
                toolCall.status = data.ok ? "completed" : "error";
                toolCall.result = data.result;
                toolCall.error = data.error;
                this.stateManager.updateTab(tabId, {});
              }
            }
          }
        }
        break;
      }
      case "approval": {
        this.stateManager.updateTab(tabId, { status: "waiting_approval" });
        break;
      }
      case "turn_end":
      case "end": {
        if (this.currentAssistantContent) {
          this.stateManager.finalizeLastAssistantMessage(tabId);
          this.currentAssistantContent = "";
        }
        break;
      }
      case "error": {
        const data = event.data;
        const errorMsg = {
          id: `msg-${Date.now()}`,
          role: "system",
          content: `\u274C Error: ${data.message || data.stderr || "Unknown error"}`,
          timestamp: event.timestamp
        };
        this.stateManager.addMessage(tabId, errorMsg);
        this.stateManager.updateTab(tabId, { status: "error" });
        break;
      }
    }
  }
  startNewTurn() {
    this.currentAssistantContent = "";
  }
};

// src/features/chat/controllers/ConversationController.ts
var ConversationController = class {
  constructor(stateManager) {
    this.stateManager = stateManager;
    this.streamController = new StreamController(stateManager);
  }
  sessionHandle = null;
  streamController;
  runtime = null;
  setRuntime(runtime) {
    this.runtime = runtime;
  }
  async sendMessage(text, mentions, instruction) {
    const tab = this.stateManager.getActiveTab();
    if (!tab) return;
    const userMsg = {
      id: `msg-${Date.now()}`,
      role: "user",
      content: text,
      timestamp: Date.now(),
      mentions,
      instruction
    };
    this.stateManager.addMessage(tab.id, userMsg);
    const assistantMsg = {
      id: `msg-${Date.now() + 1}`,
      role: "assistant",
      content: "",
      timestamp: Date.now() + 1,
      isStreaming: true
    };
    this.stateManager.addMessage(tab.id, assistantMsg);
    this.stateManager.updateTab(tab.id, { status: "streaming" });
    if (!this.sessionHandle && this.runtime) {
      try {
        this.sessionHandle = await this.runtime.start({
          cwd: "",
          // Will be set by main.ts
          model: tab.model || void 0,
          effort: tab.effort || void 0,
          permissionMode: tab.permissionMode
        });
        this.setupEventListeners(tab.id);
      } catch (err) {
        this.stateManager.updateTab(tab.id, { status: "error" });
        const errorMsg = {
          id: `msg-${Date.now()}`,
          role: "system",
          content: `\u274C Failed to start session: ${err instanceof Error ? err.message : String(err)}`,
          timestamp: Date.now()
        };
        this.stateManager.addMessage(tab.id, errorMsg);
        return;
      }
    }
    const input = { text, mentions, instruction };
    this.streamController.startNewTurn();
    try {
      await this.sessionHandle?.send(input);
    } catch (err) {
      this.stateManager.updateTab(tab.id, { status: "error" });
    }
  }
  async cancel() {
    await this.sessionHandle?.cancel();
  }
  async togglePlanMode() {
    const tab = this.stateManager.getActiveTab();
    if (!tab) return;
    this.stateManager.updateTab(tab.id, {
      isPlanMode: !tab.isPlanMode,
      permissionMode: tab.isPlanMode ? "default" : "plan"
    });
  }
  setupEventListeners(tabId) {
    if (!this.sessionHandle) return;
    this.sessionHandle.on("*", (event) => {
      this.streamController.handleEvent(tabId, event);
    });
  }
  getSession() {
    return this.sessionHandle;
  }
  async dispose() {
    await this.sessionHandle?.close();
    this.sessionHandle = null;
  }
};

