var PromptCompiler = class {
  constructor(settings, vaultPath) {
    this.settings = settings;
    this.vaultPath = vaultPath;
  }
  compile(input) {
    const parts = [];
    if (this.settings.appendSystemPrompt) {
      parts.push(this.settings.appendSystemPrompt);
    }
    if (input.instruction) {
      parts.push(`
[Instruction]: ${input.instruction}`);
    }
    if (input.mentions && input.mentions.length > 0) {
      const mentionParts = [];
      for (const mention of input.mentions) {
        mentionParts.push(this.resolveMention(mention));
      }
      if (mentionParts.length > 0) {
        parts.push(`
[Referenced files]:
${mentionParts.join("\n")}`);
      }
    }
    parts.push(input.text);
    return parts.join("\n");
  }
  resolveMention(mention) {
    switch (mention.type) {
      case "file":
        return `- @${mention.ref}`;
      case "mcp":
        return `- @mcp:${mention.ref}`;
      case "agent":
        return `- @agent:${mention.ref}`;
      case "extDir":
        return `- @dir:${mention.ref}`;
      default:
        return `- @${mention.ref}`;
    }
  }
};

