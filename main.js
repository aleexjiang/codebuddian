var import_child_process = require("child_process");
var import_fs = require("fs");
var import_path = require("path");
var CLI_NAMES = ["codebuddy", "cbc"];
var CliDetector = class {
  cachedPath = null;
  async detect() {
    if (this.cachedPath) return this.cachedPath;
    for (const name of CLI_NAMES) {
      try {
        const path = await this.which(name);
        if (path) {
          this.cachedPath = path;
          return path;
        }
      } catch {
      }
    }
    const home = process.env.HOME || "";
    const commonPaths = [
      (0, import_path.join)(home, ".nvm/versions/node/v25.8.1/bin/codebuddy"),
      (0, import_path.join)(home, ".nvm/versions/node/v25.8.1/bin/cbc"),
      "/usr/local/bin/codebuddy",
      "/usr/local/bin/cbc"
    ];
    for (const p of commonPaths) {
      if ((0, import_fs.existsSync)(p)) {
        this.cachedPath = p;
        return p;
      }
    }
    return null;
  }
  which(cmd) {
    return new Promise((resolve) => {
      const isWin = process.platform === "win32";
      const whichCmd = isWin ? "where.exe" : "which";
      (0, import_child_process.execFile)(whichCmd, [cmd], (err, stdout) => {
        if (err) {
          resolve(null);
        } else {
          const path = stdout.trim().split("\n")[0].trim();
          resolve(path || null);
        }
      });
    });
  }
  async getVersion(cliPath) {
    const path = cliPath || this.cachedPath;
    if (!path) return "unknown";
    return new Promise((resolve) => {
      (0, import_child_process.execFile)(path, ["--version"], (err, stdout) => {
        if (err) resolve("unknown");
        else resolve(stdout.trim());
      });
    });
  }
  clearCache() {
    this.cachedPath = null;
  }
};

